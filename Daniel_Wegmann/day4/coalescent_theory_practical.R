# with mutation, the average tree height is much smaller than without mutation. 
# This is because with mutation, the coalescent events are more recent, 
# leading to shorter branch lengths and thus a smaller average tree height.
# Coalescent events are more recent because mutations introduce new lineages, 
# Therefore, the time to the most recent common ancestor is shorter, resulting in a smaller average tree height.
# The theory behind this is that the coalescent process is affected by the mutation rate.

