# Q1) Given a constant population of size 2N = 100, 
# what is the probability that two individuals pick the same parent one generation before? 
# What that they pick the same parent two generations before?

# the probability that two individuals pick the same parent one generation before 
# is P(t2 = 1) = 1/(2N) = 1/100 = 0.01.

# the probability that two individuals pick the same parent two generations before
# is P(t2 = 2) = (1 - 1/(2N)) * (1/(2N)) = (1 - 1/100) * (1/100) = (99/100) * (1/100) = 0.0099.

# Q2) A researcher sequences a DNA fragment of length 10kb of a diploid individual 
# and detects 21 heterozygous sites. Assume that μ (mutation rate) = 10−9 per site for this segment 
# and that the assumptions of the standard coalescent and the infinites sites model hold.

# a) what is the effective population size? 

# under the infite sites model, E[π] = 4Neμ. Where pi is the average number of 
# pairwise differences between two sequences.
# therefore to estimate effective population size Ne = π/(4μ).
# Here, π = 21/10000 = 0.0021 and μ = 10^-9, therefore Ne = 0.0021/(4*10^-9) = 525000.
# The 21 is the number of heterozygous sites, and the 10kb is the length of the DNA fragment.
# So an estimate of Ne is 525,000.

# b) what is the expected time to the most recent common ancestor (TMRCA) of the two sequences?

# under the standard coalescent, E[TMRCA] = 2Ne generations.
# therefore, E[TMRCA] = 2 * 525000 = 1050000 generations.

